"""
Tipo NONE

O tipo de dado NONE em python respresenta o tipo sem tipo, ou poderia ser conhecido também como tipo vazio, porém, falar
que é um tipo sem tipo é mais apropriado,

obs: O tipo NONE é sempre especificado com a primeira letra maiúscula.

Certo: None
Errado: none

Quando utilizamos?

- Podemos utilizar None quando queremos criar uma variável e inicializa-la com um tipo sem tipo, antes
de recebermos um valor final.

obs
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67

print(numeros)
print(type(numeros))
